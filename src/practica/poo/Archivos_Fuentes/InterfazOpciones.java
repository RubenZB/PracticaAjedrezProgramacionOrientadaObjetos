    private void subirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_subirActionPerformed
        JFileChooser archivos = new JFileChooser();
        this.setIconImage(Toolkit.getDefaultToolkit().getImage("iconocaballo.jpg"));
        FileFilter filtro = new FileNameExtensionFilter("Archivos text (.txt)", "txt");
        archivos.setFileFilter(filtro);
        int status = archivos.showOpenDialog(null);
        if (status == archivos.APPROVE_OPTION) {
            String ruta = archivos.getSelectedFile().getAbsolutePath();
            try {
                aProblemas.subirProblema(ruta);
            } catch (MiExcepcion ex) {
                Logger.getLogger(InterfazOpciones.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else if (status == archivos.CANCEL_OPTION) {
            archivos.setVisible(false);
        }
    }//GEN-LAST:event_subirActionPerformed

