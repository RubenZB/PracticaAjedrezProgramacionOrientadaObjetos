    private void jugarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jugarActionPerformed
        try {
            int valorDado = ThreadLocalRandom.current().nextInt(0, aProblemas.getProblemas().size());
            new InterfazTablero(aUsuario, aProblemas, indice, valorDado).setVisible(true);
        } catch (MiExcepcion ex) {
            Logger.getLogger(InterfazOpciones.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.setVisible(false);

    }//GEN-LAST:event_jugarActionPerformed

