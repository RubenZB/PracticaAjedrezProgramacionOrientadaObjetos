    private void ComprobarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ComprobarActionPerformed
        String sol = solucion.getText();
        boolean jugar = aProblemas.getProblemas().get(numProblema).jugar(sol, resuelto);
        if (!resuelto) {
            aUsuario.getUsuarios().get(indice).intento(jugar, numProblema);
        }
        if (jugar) {
            JOptionPane.showMessageDialog(null, "Respuesta correcta");
            new InterfazOpciones(aUsuario, aProblemas, indice).setVisible(true);
            this.setVisible(false);
        } else {
            JOptionPane.showMessageDialog(null, "Respuesta incorrecta. Prueba otra vez");
            this.solucion.setText("");
        }


    }//GEN-LAST:event_ComprobarActionPerformed

